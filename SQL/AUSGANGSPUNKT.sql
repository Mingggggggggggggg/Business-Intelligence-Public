CREATE TABLE ED_KONTOART
(
	KART_ID		    char(2)		 not null,
	KA_Bezeichnung	varchar(20)	 not null,
	CONSTRAINT PK_ED_KONTOART PRIMARY KEY (KART_ID)
)

CREATE TABLE ED_BUCHUNGSART
(
	BART_ID		    char(1)		 not null,
	B_Bezeichnung	varchar(20)	 not null,
	CONSTRAINT PK_ED_BUCHUNGSART PRIMARY KEY (BART_ID)
)

CREATE TABLE ED_KUNDENTYP
(
	KTYP_ID		    char(2)		 not null,
	KT_Bezeichnung	varchar(20)	 not null,
	CONSTRAINT PK_ED_KUNDENTYP PRIMARY KEY (KTYP_ID)
)

CREATE TABLE ED_KREDITINSTITUTART
(
	KIA_ID		    char(1)		 not null,
	KIA_Bezeichnung	varchar(50)	 not null,
	CONSTRAINT PK_ED_KREDITINSTITUTART PRIMARY KEY (KIA_ID)
)

CREATE TABLE ED_ADRESSE
(
	AD_ID		    char(5)		 not null,
	Strasse			varchar(30)	 not null,
	Postleitzahl	char(5)		 not null,
	Stadt			varchar(30)  not null,
	Land			varchar(30)	 not null,
	CONSTRAINT PK_ED_ADRESSE PRIMARY KEY (AD_ID)
)

CREATE TABLE ED_KUNDE
(
	Kundennummer			char(5)			not null,
	Kundentyp				char(2)			not null
		REFERENCES ED_KUNDENTYP (KTYP_ID),
	Name				    varchar(50)		not null,
	Vorname					varchar(50)		    null,
	Geschlecht				char(1)			    null	CHECK(Geschlecht = 'M' OR Geschlecht = 'W' OR Geschlecht = 'D'),
	Geburtsdatum			date    		    null,
	AD_ID			        char(5)		    not null
		REFERENCES ED_ADRESSE (AD_ID),
	CONSTRAINT PK_ED_KUNDE PRIMARY KEY (KUNDENNUMMER)
)

CREATE TABLE ED_KREDITINSTITUT
(
	BIC			char(11)		not null,
	Bankname	char(50)		not null,
	KIA_Art      char(1)         not null
		REFERENCES ED_KREDITINSTITUTART (KIA_ID),
	CONSTRAINT PK_ED_KREDITINSTITUT PRIMARY KEY (BIC)
)

CREATE TABLE ED_KONTO
(
	IBAN			char(27)		 not null,
	BIC				char(11)		 not null
		REFERENCES ED_KREDITINSTITUT (BIC),
	Kontoart		char(2)			 not null
		REFERENCES ED_KONTOART (KART_ID),
	Kontoinhaber	char(5)		     not null
		REFERENCES ED_KUNDE (KUNDENNUMMER),
	Kontostand		money			     null,
	Sollzins		float			     null,
	Habenszins		float				 null,
	CONSTRAINT PK_ED_KONTO PRIMARY KEY (IBAN)
)

CREATE TABLE ED_BUCHUNG
(
	Buchungsnummer		int				 not null,
	Buchungsart			char(1)			 not null
		REFERENCES ED_BUCHUNGSART (BART_ID),
	Buchungsdatum		date			 not null,
	Sender				char(27)			 null
		REFERENCES ED_KONTO (IBAN),
	Empfaenger			char(27)			 null
		REFERENCES ED_KONTO (IBAN),
	Betrag				money			 not null,
	Verwendungszweck	varchar(140)	     null,
	CONSTRAINT PK_ED_BUCHUNG PRIMARY KEY (BUCHUNGSNUMMER)
)

